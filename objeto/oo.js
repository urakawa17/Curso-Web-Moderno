// 1. Diferenças dos paradigmas de programação
//     1. Não estruturada → Go to, direcionando comportamentos do sistema transitando entre seções do código
//     2. Estruturada:
//         1. Procedural: Programação focada em procedimentos
        
//         processamento(valor 1, valor 2…)
        
//         2. Orientada à Objetos: Programação focada em objetos, os quais possuem dados e funções/ações
        
//         objeto = {
//         nome,
//         descricao,
//         ação1,
//         ação2,
//         …
//         }
        
//         Princípios importantes da Programação OO:
        
//         1. Abstração: Com base na sua necessidade, ter a capacidade de avaliar o cenário e abstrair, isolar características, atributos, significados, comportamentos, de forma que seja coerente com o sistema a ser desenvolvido;
//         2. Encapsulamento: Encapsular, esconder detalhes da implementação, tornando exposta uma aplicação que tenha uma capacidade maior de adaptação e manutenção, sem impactar a sua operabilidade e com o menor nível de dependencia possível entre os componentes que formam o sistema no sentido de engessar os módulos;
//         3. Herança (Prototype): Possibilidade de herdar atributos, características de um objeto para outro. Priorizar composição ao invés de herança;
//         4. Polimorfismo: Mais presente em linguagens tipadas, capacidade de transformar objetos genericos em objetos especificos;